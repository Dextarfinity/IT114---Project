<script setup>
import { supabase } from "../supabaseClient";
import Navbar from "@/components/body.vue";
import { ref } from "vue";
import { useRouter } from "vue-router";

// Toast handling function
const toastMessage = ref("");
const toastColor = ref("");
const toastVisible = ref(false);

const showToast = (message, type) => {
  toastMessage.value = message;
  toastColor.value =
    type === "success" ? "bg-green-500 text-white" : "bg-red-500 text-white";
  toastVisible.value = true;
  setTimeout(() => {
    hideToast();
  }, 3000);
};

const hideToast = () => {
  toastVisible.value = false;
};

const onTransitionEnd = () => {
  if (!toastVisible.value) {
    toastMessage.value = "";
  }
};

const router = useRouter(); // Use the router instance

// Reactive variables
const email = ref("");
const password = ref("");
const confirmPassword = ref("");
const emailError = ref("");
const passwordError = ref("");
const confirmPasswordError = ref("");

// Auto-fill email from current session
const loadUserEmail = async () => {
  try {
    const { data: { user }, error } = await supabase.auth.getUser();
    if (user && user.email) {
      email.value = user.email;
    }
  } catch (error) {
    console.error("Error loading user email:", error);
  }
};

// Load email on component mount
loadUserEmail();

// Email validation logic
const validateEmail = () => {
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  if (!email.value.trim()) {
    emailError.value = "Email cannot be empty.";
  } else if (!emailRegex.test(email.value)) {
    emailError.value = "Invalid email format.";
  } else {
    emailError.value = "";
  }
};

// Password validation logic
const validatePassword = () => {
  try {
    if (!password.value.trim()) throw new Error("Empty-password");
    if (password.value.length < 8) throw new Error("shortPassword");
    if (!/[A-Z]/.test(password.value)) throw new Error("noUppercase");
    if (!/[a-z]/.test(password.value)) throw new Error("noLowercase");
    if (!/[0-9]/.test(password.value)) throw new Error("noDigit");
    if (!/[!@#$%^&*(),.?":{}|<>]/.test(password.value))
      throw new Error("noSpecialCharacter");
    passwordError.value = "";
    return true;
  } catch (error) {
    passwordError.value = error.message;
    return false;
  }
};

// Confirm password validation
const validateConfirmPassword = () => {
  if (confirmPassword.value !== password.value) {
    confirmPasswordError.value = "Passwords do not match.";
  } else {
    confirmPasswordError.value = "";
  }
};

// Handle form submission
const handleSubmit = async () => {
  validateEmail();
  const isPasswordValid = validatePassword();
  validateConfirmPassword();

  if (!emailError.value && isPasswordValid && !confirmPasswordError.value) {
    try {
      console.log("Updating password...");

      // Update only the password (email shouldn't be changed during password reset)
      const { data, error } = await supabase.auth.updateUser({
        password: password.value,
      });

      if (error) {
        console.error("Error updating password:", error.message);
        showToast(`Error: ${error.message}`, "error");
      } else {
        console.log("Password updated successfully:", data);
        showToast("Password updated successfully! Redirecting to login...", "success");
        
        // Sign out the user after password update
        await supabase.auth.signOut();
        
        // Clear form fields
        email.value = "";
        password.value = "";
        confirmPassword.value = "";
        
        // Redirect to login after a delay
        setTimeout(() => {
          router.push("/auth");
        }, 2000);
      }
    } catch (error) {
      console.error("Unexpected error:", error);
      showToast("An unexpected error occurred. Please try again.", "error");
    }
  } else {
    console.log("Form contains errors. Please fix them before submitting.");
    showToast("Please fix the errors before submitting the form.", "error");
  }
};
</script>

